import { AnswerEvaluation } from '../../types/evaluation';
import { ConversationMemory } from '../../types/conversationMemory';
import { DifficultyLevel } from '../../types/adaptive';

export interface ScoringInput {
  baseScore: number;
  answerText: string;
  difficulty: DifficultyLevel;
  memory: ConversationMemory;
  expectedSkills?: string[];
}

export const evaluateAnswer = (input: ScoringInput): AnswerEvaluation => {
  const { baseScore, answerText, difficulty, expectedSkills = [] } = input;
  
  // Heuristic variations based on word count
  const wordCount = answerText.split(/\s+/).filter(w => w.length > 0).length;
  
  // Base modifier from length (too short = penalty, good length = bonus)
  let completenessMod = 0;
  if (wordCount < 10) completenessMod = -20;
  else if (wordCount < 30) completenessMod = -10;
  else if (wordCount > 100) completenessMod = +10;
  else completenessMod = +5;

  // Base modifier from difficulty
  let depthMod = 0;
  if (difficulty === 'Advanced' || difficulty === 'Senior' || difficulty === 'Expert') {
    depthMod = (baseScore > 75) ? +10 : -10;
  }

  // Calculate distinct scores by applying slight stochastic/heuristic bounds to the base score
  const clamp = (val: number) => Math.max(0, Math.min(100, Math.round(val)));
  
  // Communication: higher for well-structured long sentences, we approximate with length-to-base correlation
  const communicationScore = clamp(baseScore + (wordCount > 50 ? 5 : -5));
  
  // Technical Accuracy: tightly bound to the base score generated by Gemini
  const technicalAccuracy = clamp(baseScore + (expectedSkills.length > 0 ? 5 : 0));
  
  // Completeness
  const completeness = clamp(baseScore + completenessMod);
  
  // Confidence: inferred from length and base score
  const confidence = clamp(baseScore * 0.8 + (wordCount > 40 ? 20 : 10));

  // Default values drifting around base score
  return {
    technicalAccuracy,
    communication: communicationScore,
    completeness,
    correctness: baseScore,
    depthOfKnowledge: clamp(baseScore + depthMod),
    problemSolving: clamp(baseScore * 0.9 + 5),
    confidence,
    structure: clamp(communicationScore - 5),
    vocabulary: clamp(communicationScore + 5),
    industryKnowledge: clamp(baseScore + depthMod),
    behavioralThinking: clamp((baseScore + communicationScore) / 2),
    leadership: clamp(baseScore - 10), // Usually lower unless explicitly requested
    decisionMaking: clamp(baseScore),
    criticalThinking: clamp(baseScore + depthMod),
    baseScore
  };
};
